export interface FinalizeReleaseParams {
  leaseId:      BN;
  operatorKeypair: Keypair;   // operator signs finalize_release
  vaultSession: PublicKey;
  guardianSet:  PublicKey;
  asset:        PublicKey;
  collection:   PublicKey;
}

/**
 * Build and send monasol_protocol::finalize_release.
 * Signed by the operator keypair (not the oracle).
 * Triggers CPIs: vault_key::move_out + guardian_multisig::close_guardian_set.
 */
export async function finalizeRelease(
  params: FinalizeReleaseParams
): Promise<TransactionSignature> {
  const { monasol } = getPrograms();

  const protocolState = deriveProtocolState();
  const sessionRecord = deriveSessionRecord(params.leaseId);
  const protocolAuth  = deriveProtocolAuth();

  // finalize_release requires both operator (authority) and payer
  // The operator keypair must be the registered protocol authority
  const sig = await monasol.methods
    .finalizeRelease()
    .accountsStrict({
      operator:                params.operatorKeypair.publicKey,
      payer:                   params.operatorKeypair.publicKey,
      protocolState,
      authority:               params.operatorKeypair.publicKey,
      sessionRecord,
      protocolAuth,
      vaultSession:            params.vaultSession,
      asset:                   params.asset,
      collection:              params.collection,
      guardianSet:             params.guardianSet,
      vaultKeyProgram:         VAULT_KEY_PROGRAM_ID,
      guardianMultisigProgram: GUARDIAN_MULTISIG_PROGRAM_ID,
      mplCoreProgram:          MPL_CORE_PROGRAM_ID,
      systemProgram:           SystemProgram.programId,
    })
    .signers([params.operatorKeypair])
    .rpc();

  logger.info(
    {
      sig,
      leaseId:      params.leaseId.toString(),
      sessionRecord: sessionRecord.toBase58(),
    },
    "oracle: finalize_release confirmed"
  );

  return sig;
}

// -----------------------------------------------------------------------------
// Account fetchers — for route response hydration
// -----------------------------------------------------------------------------

export async function fetchSessionRecord(leaseId: BN) {
  const { monasol } = getPrograms();
  const sessionRecord = deriveSessionRecord(leaseId);

  try {
    return await monasol.account.sessionRecord.fetch(sessionRecord);
  } catch {
    return null;
  }
}

export async function fetchGuardianSet(leaseId: BN) {
  const { guardian } = getPrograms();
  const guardianSet = deriveGuardianSet(leaseId);

  try {
    return await guardian.account.guardianSet.fetch(guardianSet);
  } catch {
    return null;
  }
}

export async function fetchVaultSession(
  renter: PublicKey,
  collection: PublicKey
) {
  const { vaultKey } = getPrograms();
  const vaultSession = deriveVaultSession(renter, collection);

  try {
    return await vaultKey.account.vaultSession.fetch(vaultSession);
  } catch {
    return null;
  }
}
