// -----------------------------------------------------------------------------
// Discriminator computation
// Anchor discriminator = sha256("global:<instruction_name>")[0..8]
// Use this to verify and populate the CPI byte arrays in monasol_protocol.
// -----------------------------------------------------------------------------

export function computeDiscriminator(instructionName: string): Buffer {
  const hash = createHash("sha256")
    .update(`global:${instructionName}`)
    .digest();
  return hash.subarray(0, 8);
}

/**
 * Print all 4 discriminators needed by monasol_protocol CPI builders.
 * Call this once after first build to get the real values.
 *
 * Usage in test:
 *   printCpiDiscriminators();
 */
export function printCpiDiscriminators(): void {
  const instructions = [
    "thaw_key",
    "move_out",
    "mark_settled",
    "close_guardian_set",
  ];

  console.log("\n=== CPI Discriminators for monasol_protocol ===");
  for (const ix of instructions) {
    const disc = computeDiscriminator(ix);
    const bytes = Array.from(disc)
      .map((b) => `0x${b.toString(16).padStart(2, "0")}`)
      .join(", ");
    console.log(`${ix.padEnd(24)} → [${bytes}]`);
  }
  console.log("================================================\n");
}

