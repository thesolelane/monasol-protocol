// -----------------------------------------------------------------------------
// Balance assertion helper
// -----------------------------------------------------------------------------

export async function assertBalanceIncreased(
  connection: Connection,
  pubkey: PublicKey,
  before: number,
  label = ""
): Promise<void> {
  const after = await connection.getBalance(pubkey);
  if (after <= before) {
    throw new Error(
      `${label} balance did not increase: before=${before} after=${after}`
    );
  }
}

