/** Generate N fresh keypairs. */
export function makeKeypairs(n: number): Keypair[] {
  return Array.from({ length: n }, () => Keypair.generate());
}

/**
 * Build a standard 5-member guardian array.
 * Returns { shardMembers: [kp0, kp1], approvers: [kp2, kp3, kp4] }
 * and a flat `members` array in the correct positional order.
 */
export function makeGuardianCouncil(): {
  shardMembers: [Keypair, Keypair];
  approvers:    [Keypair, Keypair, Keypair];
  members:      PublicKey[];
} {
  const [s0, s1, a0, a1, a2] = makeKeypairs(5);
  return {
    shardMembers: [s0, s1],
    approvers:    [a0, a1, a2],
    members:      [s0.publicKey, s1.publicKey, a0.publicKey, a1.publicKey, a2.publicKey],
  };
}

/**
 * Build a mock 32-byte Monad transaction hash for testing.
 * In prod this comes from the Monad RPC.
 */
export function mockMonadTxHash(seed = "test"): number[] {
  const hash = createHash("sha256").update(seed).digest();
  return Array.from(hash);
}

/**
 * Build a mock shard commitment:
 *   SHA-256(fragment_bytes || lease_id_le_bytes)
 * Mirrors the off-chain shard commitment construction.
 */
export function mockShardCommitment(
  fragmentSeed: string,
  leaseId: BN
): number[] {
  const fragment = Buffer.from(fragmentSeed);
  const leaseIdBytes = leaseId.toArrayLike(Buffer, "le", 8);
  const hash = createHash("sha256")
    .update(Buffer.concat([fragment, leaseIdBytes]))
    .digest();
  return Array.from(hash);
}

